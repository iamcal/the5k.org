id="about2002"><h1 class="pageTitle">About the 5k</h1><p>The purpose of the competition was expressed on the original contest site thusly:<blockquote>The idea behind the contest is that the 
rigid constraints of designing for the web are what force us to get truly creative. Between servers and bandwidth, clients and users, HTML and the DOM, browsers and platforms, our conscience 
