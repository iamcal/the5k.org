makes it all interesting. Just celebrating that is all.</blockquote></p><p>The 5k contest was conceived of by <a href="http://www.sylloge.com">Stewart Butterfield</a> in the fall of 1999 and 
the first competition was launched in early 2000. It is now run by Stewart and <a href="http://www.glish.com">Eric Costello</a>. (You may contact them using <a href="/contact.php">this 
form</a>.) The design for the 2002 site was created by <a href="http://caterina.net/">Caterina Fake</a>. You'll note that it only uses black and white pixels ("look Mom, no gray!")</p><p>The 
5k competition is entirely non-commercial and does not accept sponsorship or advertising. It will continue on an annual basis for the foreseeable future and hopefully the goals of the 
contest will continue to be fulfilled: an exploration of what's possible with relatively little information, an opportunity to learn from everyone else's explorations (both in terms of 
technique and imagination) and recognition of some of the most talented designers, developers and programmers around.</p> </div></td></tr>

